    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jList1.getSelectedIndex() != -1) {
            String filename = (String) listModel.getElementAt(jList1.getSelectedIndex());
            String[] thisSession = (String[]) ObjectReaderWriter.ReadObject(savepath + filename + ".ser");
            
            jTextField1.setText(thisSession[0]);
            jTextField3.setText(thisSession[1]);
            jTextField2.setText(thisSession[2]);
            if (thisSession[3].equals("1")) 
                jRadioButton1.doClick();
            else if (thisSession[3].equals("2"))
                jRadioButton2.doClick();
            else if (thisSession[3].equals("3"))
                jRadioButton3.doClick();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (!jTextField4.getText().equals("")) {
            user = jTextField1.getText();
            host = jTextField3.getText();
            portStr = jTextField2.getText();
            connection = jRadioButton1.isSelected()? "1" : jRadioButton2.isSelected()? "2" : jRadioButton3.isSelected()? "3" : "";
            
            listModel.addElement(jTextField4.getText());
            jList1.setModel(listModel);
            
            String[] str = {user, host, portStr, connection};
            ObjectReaderWriter.WriteObject(str, savepath + jTextField4.getText() + ".ser");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (jList1.getSelectedIndex() != -1) {
            String filename = (String) listModel.getElementAt(jList1.getSelectedIndex());

            Path path = Paths.get(savepath + filename + ".ser");
            try {
                Files.delete(path);
            } catch (NoSuchFileException x) {
                System.err.format("%s: no such" + " file or directory%n", path);
            } catch (DirectoryNotEmptyException x) {
                System.err.format("%s not empty%n", path);
            } catch (IOException x) {
                // File permission problems are caught here.
                System.err.println(x);
            }
            listModel.remove(jList1.getSelectedIndex());
            jList1.setModel(listModel);
        }
    }//GEN-LAST:event_jButton5ActionPerformed


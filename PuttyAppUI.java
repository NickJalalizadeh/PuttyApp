    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        user = jTextField1.getText();
        host = jTextField3.getText();
        pass = String.valueOf(jPasswordField1.getPassword());
        port = Integer.parseInt(jTextField2.getText());
        
        if (jRadioButton1.isSelected()) {
            SecureShell.SSHConnect(user,host,pass,port);
        }
        else if (jRadioButton2.isSelected()) {
            try {
                Telnet.TelnetConnect(host, port);
            } catch (Exception ex) {
                System.err.print("Unable to connect to Telnet: " + ex.getMessage());
            }
        }
        else if (jRadioButton3.isSelected()) {
            try {
                TCPClient.TCPConnect(host, port);
            } catch (IOException ex) {
                System.err.print("Unable to connect to TCP Server: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

